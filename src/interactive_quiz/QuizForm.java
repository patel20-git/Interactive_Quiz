    public void setQuestion() {
        jLabel5.setText(question.getQuestion());
        jRadioButton1.setText(question.getOption1());
        jRadioButton2.setText(question.getOption2());
        jRadioButton3.setText(question.getOption3());
        jRadioButton4.setText(question.getOption4());
        jLabel3.setText(String.valueOf(Qcount++));

    }

    public void score() {
        String answer = jRadioButton1.isSelected() ? jRadioButton1.getText() : jRadioButton2.isSelected() ? jRadioButton2.getText() : jRadioButton3.isSelected() ? jRadioButton3.getText() : jRadioButton4.isSelected() ? jRadioButton4.getText() : "";
        question.setUserAnswer(answer);
        if (question.getAnswer().equals(question.getUserAnswer().trim())) {
            score += 1;
            //ans=true;
            question.setIsTrue(true);
        }

        // System.out.println("Score : "+score);
    }

