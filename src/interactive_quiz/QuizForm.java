    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        score();
        JOptionPane.showMessageDialog(null, "Your Score is: " + score, "Quiz Score", 2);
        String insert = "INSERT INTO quiz_question(quiz_id,question_id,answer,user_answer) VALUES (?,?,?,?)";
        try {
            PreparedStatement ps = connex.getConnection().prepareStatement(insert);
            
            for (Question question1 : questions) {
                ps.setInt(1, quizId);
                ps.setInt(2, question1.getId());
                ps.setInt(3, question1.isIsTrue()?1:0);
                ps.setString(4, question1.getUserAnswer());
                ps.addBatch();
            }
            ps.executeBatch();
            
            String update = "UPDATE quiz SET score = ? WHERE quiz_id = ?";
            ps = connex.getConnection().prepareStatement(update);
            ps.setInt(1, score);
            ps.setInt(2, quizId);
            ps.executeUpdate();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        MenuForm form = new MenuForm(user);
        form.setVisible(true);
        form.pack();
        form.setLocationRelativeTo(null);
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

